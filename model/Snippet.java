package model;

public class Snippet {
	
	private String snippetText;
	
	//コンストラクタ
	public Snippet(String snippetText) {
		this.snippetText = snippetText;
	}

	public String getSnippetText() {
		return snippetText;
	}

	public void setSnippetText(String snippetText) {
		this.snippetText = snippetText;
	}

}
